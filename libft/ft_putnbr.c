#include "libft.h"

void	ft_putnbr(int n)
{
	char s;
	char p;

	if (n == -2147483648)
	{
		write(1, "-", 1);
		write(1, "2", 1);
		ft_putnbr(n % 1000000000 * (-1));
	}
	else if (n < 0)
	{
		write(1, "-", 1);
		ft_putnbr(-n);
	}
	else if (n > 9)
	{
		ft_putnbr(n / 10);
		s = n % 10 + 48;
		write(1, &s, 1);
	}
	else
	{
		p = n + 48;
		write(1, &p, 1);
	}
}
