#include "libft.h"

void	ft_free_str(char **s)
{
	int			i;

	i = 0;
	while (s[i] != 0)
		ft_memdel((void **)(s + i++));
	ft_memdel((void **)(s + i));
	ft_memdel((void **)&s);
}
