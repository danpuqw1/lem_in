#include "libft.h"

int		ft_isalpha(int c)
{
	int ups;
	int los;
	int i;

	ups = (int)'A';
	los = (int)'a';
	i = 0;
	while (i++ < 26)
	{
		if (c == ups++)
			return (1);
		if (c == los++)
			return (1);
	}
	return (0);
}
