#include "libft.h"

long long int	ft_pow(int nb, int p)
{
	long long int	res;
	int				i;

	res = 1;
	i = 0;
	if (nb == 0 || p < 0)
		return (0);
	while (i < p)
	{
		res *= nb;
		i++;
	}
	return (res);
}
