#include "libft.h"

char	*ft_rot_n(char *s, int n)
{
	int i;

	i = 0;
	while (s[i])
	{
		if (s[i] >= 'a' && s[i] <= 'z')
			s[i] = ((s[i] - 97) + n) % 26 + 97;
		if (s[i] >= 'A' && s[i] <= 'Z')
			s[i] = ((s[i] - 65) + n) % 26 + 65;
		i++;
	}
	return (s);
}
