#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	char	*res;

	res = &s1[ft_strlen(s1)];
	i = 0;
	while (s2[i] && i < n)
	{
		res[i] = s2[i];
		i++;
	}
	res[i] = '\0';
	return (s1);
}
